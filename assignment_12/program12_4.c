#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name   : SumEvenFactors
//  Description     : It is used to accept number and print sum of its even factors
//  Input           : Int
//  Output          : Int
//  Author          : Rucha Hanumant Pawar
//  Date            : 6/12/2025
//  
//////////////////////////////////////////////////////////////////////////////////////////////////

int SumEvenFactors(int iNo)
{
    int iCnt = 0;
    int iSum = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt <= iNo/2; iCnt++)
    {
        if((iNo % iCnt == 0) && (iCnt % 2 == 0))
        {
            iSum = iSum + iCnt;
        }
    }

    return iSum;
} // End of SumEvenFactors

//  Time Complexity :   O(N/2)

//////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
//////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter number: ");
    scanf("%d", &iValue);

    iRet = SumEvenFactors(iValue);
    printf("Sum of even factors of %d are: %d", iValue, iRet);

    return 0;
}

