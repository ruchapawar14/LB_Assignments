#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name   : CountLessSix
//  Description     : It is used to accept number and count frequency of such a digits which are less than 6
//  Input           : Int
//  Output          : Int
//  Author          : Rucha Hanumant Pawar
//  Date            : 6/12/2025
//  
//////////////////////////////////////////////////////////////////////////////////////////////////

int CountLessSix(int iNo)
{
    int iDigit = 0;
    int iCount = 0;
    
    while(iNo != 0)
    {
        iDigit = iNo % 10;

        if(iDigit < 6)
        {
            iCount++;
        }

        iNo = iNo / 10;
    }

    return iCount;
} // End of CountLessSix

//  Time complexity :   log(N)

//////////////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
//////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter number : ");
    scanf("%d", &iValue);

    iRet = CountLessSix(iValue);   
    printf("Frequency of digits less than 6 is : %d", iRet);
    
    return 0;
}

