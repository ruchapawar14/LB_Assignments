#include <stdio.h>

//////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name   : DisplayConvert
//  Description     : It accepts a character from user and convert case of that character
//  Input           : Char
//  Output          : Void
//  Author          : Rucha Hanumant Pawar
//  Date            : 2/12/2025
// 
//////////////////////////////////////////////////////////////////////////////////////

void DisplayConvert(char cValue)
{
    if((cValue >= 'A') && (cValue <= 'Z'))          // To convert uppercase to lowercase
    {
        printf("%c", cValue + 32);
    }
    else if((cValue >= 'a') && (cValue <= 'z'))     // To convert Lowercase to uppercase
    {
        printf("%c", cValue - 32);
    }
} // End of DisplayConvert

//////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
//////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char cValue = '\0';

    printf("Enter character : ");
    scanf("%c", &cValue);

    DisplayConvert(cValue);

    return 0;
}

