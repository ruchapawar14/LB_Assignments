#include <stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name   : DisplayFactor
//  Description     : It accepts one number from user and prints even factors of that number
//  Input           : Int
//  Output          : void
//  Author          : Rucha Hanumant Pawar
//  Date            : 2/12/2025
// 
////////////////////////////////////////////////////////////////////////////////////////////////////////

void DisplayFactor(int iNo)
{
    int iCnt = 0;
    
    if(iNo < 0)                                             // Filter
    {
        iNo = -iNo;
    }
    
    printf("1\t");

    for(iCnt = 1; iCnt <= iNo/2; iCnt++)                    
    {
        if(((iNo % iCnt) == 0) && ((iCnt % 2) == 0))        // Business logic
        {
            printf("%d\t", iCnt);
        }

    }
} // End of DisplayFactor

// Time Complexity : O(N/2)

////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function for the application
// 
////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number : ");
    scanf("%d", &iValue);

    DisplayFactor(iValue);

    return 0;
}

