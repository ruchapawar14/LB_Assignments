#include <stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////
// 
//  Function Name   : Accept
//  Description     : Accept one number from user and print that number of * on screen
//  Input           : Int
//  Output          : Void
//  Author          : Rucha Hanumant Pawar
//  Date            : 2/12/2025
// 
/////////////////////////////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
    int iCnt = 0;
    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("*");
    }
}

/////////////////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
/////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    iValue = 5;

    Accept(iValue);
    
    return 0;
}

