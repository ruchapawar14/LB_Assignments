#include <stdio.h>

///////////////////////////////////////////////////////////////////////////////
// 
//  Function Name   : DisplayName
//  Description     : It is used to accept a name from user and print that name
//  Input           : Char
//  Output          : Void
//  Author          : Rucha Hanumant Pawar
//  Date            : 2/12/2025
// 
///////////////////////////////////////////////////////////////////////////////

void DisplayName(char cName[30])
{
    printf("Your name is %s", cName);
} // End of DisplayName

//  Time complexity :   O(1)

///////////////////////////////////////////////////////////////////////////////
// 
//  Entry point function for the application
// 
///////////////////////////////////////////////////////////////////////////////

int main()
{
    char cName[30];                             // To store the full name

    printf("Enter full name : ");
    fgets(cName, sizeof(cName), stdin);         // To accept the character until the newline character comes

    DisplayName(cName);

    return 0;
}

